export const SAMPLE_SITE_CONTENT = Object.freeze({
  youtube: { ...DEFAULT_YOUTUBE },
  serviceVideos: DEFAULT_SERVICE_VIDEOS.map((item) => ({ ...item })),
  templates: DEFAULT_TEMPLATES.map((item) => ({ ...item })),
  reviews: DEFAULT_REVIEWS.map((item) => ({ ...item })),
});

export function normalizeServiceVideo(item, index = 0) {
  if (!item || typeof item !== 'object') return null;

  const title = String(item.title || '').trim();
  const url = String(item.url || '').trim();
  const videoId = String(item.videoId || '').trim();

  if (!title || (!videoId && !url)) return null;

  return {
    id: String(item.id || `service-video-${index + 1}`).trim(),
    title,
    category: String(item.category || 'Service').trim(),
    description: String(item.description || '').trim(),
    url,
    videoId,
  };
}

export function normalizeServiceVideos(list) {
  if (!Array.isArray(list)) return [];
  return list.map((item, index) => normalizeServiceVideo(item, index)).filter(Boolean);
}

export function getDisplayServiceVideos(configVideos) {
  const saved = normalizeServiceVideos(configVideos);
  return saved.length ? saved : [...DEFAULT_SERVICE_VIDEOS];
}

export function normalizeTemplate(item, index = 0) {
  if (!item || typeof item !== 'object') return null;

  const title = String(item.title || '').trim();
  if (!title) return null;

  return {
    id: String(item.id || `template-${index + 1}`).trim(),
    title,
    category: String(item.category || 'General').trim(),
    description: String(item.description || '').trim(),
    imageUrl: String(item.imageUrl || '').trim(),
    previewUrl: String(item.previewUrl || '').trim(),
    badge: String(item.badge || '').trim(),
  };
}

export function normalizeReview(item, index = 0) {
  if (!item || typeof item !== 'object') return null;

  const name = String(item.name || '').trim();
  const text = String(item.text || '').trim();
  if (!name || !text) return null;

  const rating = Math.min(5, Math.max(1, Number(item.rating) || 5));

  return {
    id: String(item.id || `review-${index + 1}`).trim(),
    name,
    role: String(item.role || '').trim(),
    company: String(item.company || '').trim(),
    rating,
    text,
    avatarUrl: String(item.avatarUrl || '').trim(),
  };
}

export function normalizeTemplates(list) {
  if (!Array.isArray(list)) return [];
  return list.map((item, index) => normalizeTemplate(item, index)).filter(Boolean);
}

export function normalizeReviews(list) {
  if (!Array.isArray(list)) return [];
  return list.map((item, index) => normalizeReview(item, index)).filter(Boolean);
}

export function getDisplayTemplates(configTemplates) {
  const saved = normalizeTemplates(configTemplates);
  return saved.length ? saved : [...DEFAULT_TEMPLATES];
}

export function getDisplayReviews(configReviews) {
  const saved = normalizeReviews(configReviews);
  return saved.length ? saved : [...DEFAULT_REVIEWS];
}

export function getDisplayYoutube(config = {}) {
  const videoId = String(config.youtubeVideoId || '').trim();
  const url = String(config.youtubeUrl || '').trim();

  if (videoId || url) {
    return {
      url,
      videoId,
      title: config.youtubeTitle || DEFAULT_YOUTUBE.title,
      subtitle: config.youtubeSubtitle || DEFAULT_YOUTUBE.subtitle,
    };
  }

  return { ...DEFAULT_YOUTUBE };
}

export function buildPublicSiteContent(config = {}) {
  const youtube = getDisplayYoutube(config);

  return {
    youtube,
    serviceVideos: getDisplayServiceVideos(config.serviceVideos),
    templates: getDisplayTemplates(config.templates),
    reviews: getDisplayReviews(config.reviews),
    usingDefaultTemplates: !normalizeTemplates(config.templates).length,
    usingDefaultReviews: !normalizeReviews(config.reviews).length,
    usingDefaultYoutube: !String(config.youtubeVideoId || config.youtubeUrl || '').trim(),
    usingDefaultServiceVideos: !normalizeServiceVideos(config.serviceVideos).length,
    updatedAt: config.updatedAt || null,
  };
}
